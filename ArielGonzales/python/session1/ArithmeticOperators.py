# Arithmetic Adittion
def arithmetic_adittion(num1, num2):
    return num1 + num2


# Arithmetic Subtraction
def arithmetic_subtraction(num1, num2):
    return num1 - num2


# Arithmetic Multiplication
def arithmetic_multiplication(num1, num2):
    return num1 * num2


# Arithmetic Division
def arithmetic_division(num1, num2):
    return num1 / num2


# Arithmetic Modulos
def arithmetic_modulos(num1, num2):
    return num1 % num2


# Arithmetic Floor Division
def arithmetic_floorDivision(num1, num2):
    return num1 // num2


# Arithmetic Adittion
print("Adittion", arithmetic_adittion(8, 5))
# Arithmetic Subtraction
print("Subtraction", arithmetic_subtraction(5, -15))
# Arithmetic Multiplication
print("Multiplication", arithmetic_multiplication(4.5, 5))
# Arithmetic Division
print("Division", arithmetic_division(81, 3))
# Arithmetic Modulos
print("Modulos", arithmetic_modulos(15, 2))
# Arithmetic Floor Division
print("Floor Division", arithmetic_floorDivision(8, 5))
